#include <stdio.h>

int main() {
    int i = 1; // Initialize counter variable i to 1

    // Loop while i is less than or equal to 5
    while (i <= 5) {
        printf("%d\n", i); // Print the current value of i
        i++; // Increment i by 1
    }

    return 0; // Indicate successful program termination
}