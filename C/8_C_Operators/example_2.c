#include <stdio.h>
int main() {
    int x = 7, y = 5;
    printf("x > y: %d\n", x > y);
    printf("x < y: %d\n", x < y);
    return 0;
}


// The '>' (greater than) and '<' (less than) operators are relational operators in C.
// They compare two values and return 1 if the comparison is true, or 0 if it is false.
// For example, 'x > y' checks if x is greater than y.
// In this code, since x = 7 and y = 5, 'x > y' evaluates to 1 (true), and 'x < y' evaluates to 0 (false).
// These operators are commonly used in conditions, loops, and expressions where comparison is needed.
